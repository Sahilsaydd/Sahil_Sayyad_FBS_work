#1.and
# if both operands are True then return True Otherwise False
print(True and True)      # T  T = T
print(True and False)     # T  F = F
print(False and True)     # F  T = F
print(False and False)    # F  F = F
print(True and False)

#2. or
# if both oprerands are false then return false otherwise True
print(False or False)       # T F T
print(False or True)        # F T T
print(True or False)        # T F T
print(True or True)         # T T T
print(False or False)      # F F = F 
print(True or True)

#3. not
#Reverse the result
print(not True)     # T = F
   # F = T
print(not False)