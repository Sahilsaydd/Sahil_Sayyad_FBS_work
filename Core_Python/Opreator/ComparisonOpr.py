### Comparison / relationnal opreators

#1. == (Equals to)
print(10==10) #true
print(10=='10') #false

#2. != (Not Equals To)
print(10!=10) #false
print("10"!=10) #true

#3. > (Grater than)
print(10>10)
print(5>10)

#4. > = (Greater than equals to)

print(10>=10)
print(10>=5)

#5 < (Less Than)
print(10<20)
print(10<10)

#6.<= (less than equal to)
print(10<=10)
print(10<=20)
 
 