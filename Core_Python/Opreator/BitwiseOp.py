##Bitwise operators 
#1. &(AND)
#If Both bits are 1 then consider 1 otherwise 0
print(13&6)

#2. | (OR)
#If both bits are 0 then consider 0 otherwise 1

print(13|6)

#3. ^(XOR) 
# IF both bits are different then consider 1 otherwise 0
print(13^60)

#4 ~(NOT)
print(~10) # -(10+1) output :-11
print(~5) # -(5+1)output : -6

#5 << (Left Shift)
print(10<<2)
print(5<<2)
print(12<<3)


#6 >> (Right shift)
print(20>>2)
print(23>3)