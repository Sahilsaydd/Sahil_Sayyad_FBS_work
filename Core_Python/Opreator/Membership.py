## membership operator
#1 .  in
#The in operator is used to check if a value exists in a sequence (like a list, tuple, or string).
list1 = [1,2,3,4,5]
print(1 in list1) #True
print(6 in list1) #false

#2 . not in
#The not in operator is used to check if a value does not exist in a sequence.
print(6 not in list1) #False
print(1 not in list1) #True